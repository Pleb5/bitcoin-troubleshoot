<h2 class="h2 text-center my-2">About Bitcoin Troubleshoot</h2>
<h4 class="h4 text-center">What is Bitcoin Troubleshoot?</h4>
<div class="card mx-10 mt-1 p-2">
    <p>
        Bitcoin Troubleshoot is an app for bitcoiners to help each other in Self Custody
        issues with free market incentives. It is essentially P2P market with an auction mechanism.
    </p>

</div>

<h4 class="h4 text-center mt-4">So how does this work in action?</h4>
<div class="card mx-10 mt-1 p-2">
    <p>
        Let's take the example of Alice who is a bitcoiner and has a problem with her node.
        She cannot connect to it from her Software Wallet. She creates a 'Ticket' in 
        Bitcoin Troubleshoot and posts it on nostr. Now anyone who has a clue how to solve her 
        problem can bid on it with an 'Offer'. This Offer contains the price of the Troubleshooting.
    </p>

    <p class="mt-2">
        Bob who is deeper down the rabbit hole finds this ticket in the feed and bids on it with an offer. 
        Alice received a bunch of offers from different bitcoiners but she trusts Bob the most and finds his 
        price affordable. She accepts Bob's Offer. They can now resolve the node issue via encrypted chat or 
        they can also just switch to a different platform for the actual Troubleshooting part.
    </p>

    <p class="mt-2">
        Bob successfully helped Alice with her problem. He can now receive the payment they agreed on. This can 
        happen on nostr or some other way. Most likely they will pay the bitcoin price through the Lightning Network 
        which is private and cheap. 
    </p>
</div>


<h4 class="h4 text-center mt-4">How is this better than other solutions?</h4>
<div class="card mx-10 mt-1 p-2">
    <p>
        There are many options for bitcoiners to find technical help. I believe however, 
        that a dedicated Nostr solution is the best way to go for several reasons:
    </p>
    <ul>
        <li>- The nostr ecosystem is already full of bitcoiners eager to help each other out</li>
        <li class="mt-1">- With Nostr you don't tie yourself to a particular individual or company. Anyone can help anyone, any time, from anywhere, without permission</li>
        <li class="mt-1">- You can use your social graph on Nostr to find trustworthy clients / troubleshooters</li>
        <li class="mt-1">- Nostr is an excellent protocol to run Peer-to-Peer Markets. Easy to switch Relays and anyone can easily build a new client</li>
        <li class="mt-1">- Nostr allows for such a symbiosis between apps that a legacy solution will never be able to provide</li>
        <li class="mt-1">- Nostr found a nice tradeoff-balance where reliable services can be built in a less centralized way, empowering end-users and fostering open competition as a result</li>
        <li class="mt-1">- With Nostr, You hold your keys to Your online identity and data, You are in control</li>
    </ul>
</div>
